create_pct_black_deciles <- function(precincts, cty) {
  df |>
    st_drop_geometry() |>
    filter(GEOID %in% precincts) |>
    mutate(city_name = cty,
           pct_black_bin = ntile(pct_black, min(10, n()))
    )
}

